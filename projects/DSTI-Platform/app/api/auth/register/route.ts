import { NextRequest, NextResponse } from "next/server";
import { prisma } from "@/lib/prisma";

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { name, email, organizationName } = body;

    // Validate required fields
    if (!name || !email) {
      return NextResponse.json(
        { error: "Name and email are required" },
        { status: 400 }
      );
    }

    // Validate email format
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(email)) {
      return NextResponse.json(
        { error: "Invalid email format" },
        { status: 400 }
      );
    }

    // Check if user already exists
    const existingUser = await prisma.user.findUnique({
      where: { email: email.toLowerCase() },
    });

    if (existingUser) {
      return NextResponse.json(
        { error: "An account with this email already exists. Please sign in instead." },
        { status: 409 }
      );
    }

    // Create new user with APPLICANT role
    const user = await prisma.user.create({
      data: {
        name,
        email: email.toLowerCase(),
        role: "APPLICANT",
      },
    });

    // If organization name provided, create organization and link to user
    if (organizationName && organizationName.trim()) {
      const organization = await prisma.organisation.create({
        data: {
          name: organizationName.trim(),
        },
      });

      // Create membership linking user to organization
      await prisma.membership.create({
        data: {
          userId: user.id,
          organisationId: organization.id,
          role: "PRIMARY_CONTACT", // Default role for the creator
        },
      });
    }

    return NextResponse.json(
      {
        success: true,
        message: "Account created successfully",
        user: {
          id: user.id,
          name: user.name,
          email: user.email,
          role: user.role,
        },
      },
      { status: 201 }
    );
  } catch (error: any) {
    console.error("Registration error:", error);
    return NextResponse.json(
      { error: "Failed to create account. Please try again." },
      { status: 500 }
    );
  }
}
